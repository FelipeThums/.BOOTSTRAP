// tipos primitivos de dados

// string - textos - qualquer aspas 
let saudacao = 'Hello, world' 
console.log(saudacao)
console.log(typeof saudacao, 'string')

// number - numero - não precisa de aspas
let age = 27
console.log(age)
console.log(typeof age, 'age')
let price = 19.99
console.log(price)
console.log(typeof price, 'price com ponto')

// boolean - true/false - (aberto/fechado)
let ligado = true
console.log(ligado)
console.log(typeof ligado, 'boolean true')
let desligado  = false
console.log(desligado)
console.log(typeof desligado, 'boolean false')

// null - não tipado
let resultado = null
console.log(resultado)
console.log(typeof resultado, 'result null')

// underfined - não definida
let status
console.log(status)
console.log(typeof status, 'status underfined')