//  operadores de comparação 

// igualdade - == > tipagem fraca
console.log(5 == '5')

// igualdade escrito - === > tipagem forte
console.log(5 === '5')

// diferente 
console.log(5 != '5')

// diferente escrito
console.log(5 !== '5')

// maior
console.log(10 > 5)

// maior ou igual
console.log(10 >= 5)

// menor
console.log(10 < 5)

// menor ou igual
console.log(10 <= 5)

